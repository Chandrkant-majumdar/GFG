class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int k)
    {
       vector<pair<int,int>>ans;
       Node *s=head,*e=head;
       while(e->next){
           e=e->next;
       }
       while(s->data<e->data){
           int sum=s->data+e->data;
           if(sum==k) {
               ans.push_back({s->data,e->data});
               s=s->next;
               e=e->prev;
           }
           else if(sum>k) e=e->prev;
           else  s=s->next;
           
       }
        return ans;
    }
};

